# operasi yang dapat di lakukan dengan meringkas 
# operasi di tambah dengan assigment

a = 5 # assigment 
print("nilai a = ", a)

a += 1 # artinya a = a + 1
print("nilai a+=1, nilai a menjadi + ", a)

a -= 1 # artinya a = a - 1
print("nilai a-=1, nilai a menjadi - ", a)

a *= 5 # artinya a = a * 5
print("nilai a*=5, nilai a menjadi * ", a)


a /= 5 # artinya a = a / 5
print("nilai a/=5, nilai a menjadi / ", a)


# modulus dan floor division
a %= 5 # artinya a = a % 5
print("nilai a%=5, nilai a menjadi % ", a)


# pangkat / exponen
a **= 5 # artinya a = a ** 5
print("nilai a**=5, nilai a menjadi ** ", a)

